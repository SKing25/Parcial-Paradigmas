    public void ListarEstudiante() {
        DefaultTableModel tablaEst = new DefaultTableModel();
        DataEstudiante objest = new DataEstudiante();
        ArrayList<DataEstudiante> listaEst = new ArrayList();
        listaEst = objest.ListaEstudiante();
        tablaEst.addColumn("Codigo");
        tablaEst.addColumn("Nombre");
        tablaEst.addColumn("Cantidad");
        tablaEst.addColumn("Estado");
        tablaEst.setRowCount(listaEst.size());
        int i = 0;
        for(DataEstudiante x : listaEst){
            tablaEst.setValueAt(x.getEs_identificacion(), i, 0);
            tablaEst.setValueAt(x.getEs_nombre_completo(), i, 1);
            tablaEst.setValueAt(x.getEs_carrera(), i, 2);
            tablaEst.setValueAt(x.getEs_Email(), i, 3);
            i++;
        }
        this.jTEstudiantes.setModel(tablaEst);
    }

